    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cycleSelected = (String) jComboBoxCycles.getSelectedItem();
        String yearSelected = (String) jComboBoxYear.getSelectedItem();
        MyConnection conexion = new MyConnection();
        List<String> datos = conexion.informacionFctPorCursoYCiclo(cycleSelected, yearSelected);
        if (datos != null) {
            int i = 0;
            for (String dato : datos) {
                switch (i) {
                    case 0:
                        jTextFieldCompanies.setText(dato);
                        break;
                    case 1:
                        jTextFieldNumFcts.setText(dato);
                        break;
                    default:
                        break;
                }
                i++;
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

